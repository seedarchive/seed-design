<h3>The Human Condition in the 21st Century</h3><br/>

<p>The quality of life for growing numbers of people on the planet is
threatened by a set of systemic problems: dependence on fossil fuels,
pressures for unrealistic levels of economic growth, inequitable
distribution of wealth and income, excesses and hidden costs of
consumerism, and the undue influence of global corporations over working
conditions, social wellbeing, and governing institutions. Growing economic
inequality produces poor health and precarious life prospects for
majorities in the global south and for increasing numbers in the
north. Billions of people in the global south face food and water
shortages, which in addition to political corruption and climate change,
contribute to failed states, wars, and migration.</p>

<p>Not only does the current economic regime fail to provide prosperity for
enough people, but the addiction to economic growth -- driven by business,
governments, and consumers -- is killing the life support capacity of the
planet. We are currently undergoing the sixth great species extinction in
the history of the planet, and the first one caused by a species:
humans. The most direct means of promoting greater prosperity for people on
a more thriving planet is to change the current economic model to one that
favors sustainable production and consumption and equitable
distribution. Indeed, the environmental movement could better attain many
of its goals by advocating for sustainable economics over the current menu
of environmental policies. Similarly but on a smaller scale, the debates
within the CHI sustainability community about how to move toward genuine
sustainability could benefit from such an economic focus as well
[1]. Meanwhile, many democracies have trouble addressing either economic or
environmental justice issues, in part because political parties have been
captured by business interests, and because professional politicians have
lost touch with many younger citizens who prefer direct action to tired
ideas and empty promises [2]. </p>

<p>A shift to a people and environmentally friendly economic regime will
involve politics, which means returning democracies to the people, and
holding policy-makers accountable to popular movements aligned around an
agenda of solutions to pressing problems of the environment,
economy, and democracy (SEED). We
propose to build such a global thought network by engaging currently
scattered local and national movements around the prospects of building
common narratives and action plans. The logic for developing the SEED
thought/action network is outlined in the second half of this article,
along with an invitation for reader interest and engagement. </p>

<h3>Narratives for Prosperity</h3><br/>

<p>If democracies are to lead the way in finding new models for human
wellbeing within environmental limits, nothing short of a renewal of
politics and economics is required. Political systems once regarded as
mechanisms for solving problems are now widely regarded as part of the
problem. And the growth-centred models coming from most economics
departments must be countered by well developed alternative economic theory
that has been available just outside the mainstream for decades
[3,4]. Beyond promoting models of sustainable economics, it is time to
challenge the prevailing magical thinking that deregulated markets somehow
possess intelligence, create the best outcomes, and make people
free. Similarly dubious assumptions about the virtues of GDP growth, no
matter the form or the distribution, continue to be recited by economists,
media pundits, and leading politicians. Many social democratic and labour
parties have been hollowed out by the lure of neoliberalism, abandoning
their legacy of forging better social contracts between business and
working people.</p>

<p>Adding to these problems, consumerism has become the most widely shared
cultural scheme on the planet, crossing boundaries of ideology, religion,
race, gender, and class. Advertising exhorts people to buy things they do
not need and often cannot afford. The advertised attractions of consumer
lifestyles distract attention from the unsustainable realities of a broken
system: consumer debt, emotional stress, poor labor conditions,
displacement of other social values, and continuing dependence on fossil
fuels for producing and distributing goods that are designed to go out of
fashion.</p>

<p>The current dystopian vision -- built around speculative markets, growth at
any cost, better representation for the rich, and equating the interests of
corporations with the wellbeing of people -- is surely not the best that
contemporary civilization can offer. Rather than allow these chaotic
systems to produce greater dysfunction, the present moment can become an
opportunity for creative solutions. It is time to think about what kinds of
economic production and consumption are most desirable, and how it should
be distributed. Economic growth based on speculation must be replaced by
investments that produce greater social wellbeing. More equitable economic
formulas must also be held accountable for addressing resource depletion
and declining environmental quality. </p>

<p>Creating social, cultural and economic systems that put people,
communities, and the planet first requires reimagining the simple elements
of the good life. What helps people prosper?  The basic elements of
prosperity include food, shelter, health, education, security, leisure,
participation, creative expression, and freedom from violence and
oppression, among others.  The challenge is to find new political and
economic models that focus on such basic human values, and to organize
politics to deliver these results. </p>

<p>It is time to bridge these three critically related areas of democracy,
economics, and environmental policy with a broad agenda for change that
combines input from academics, activists, civil society organizations,
parties, and concerned citizens.</p>

<h3>What Is To Be Done?</h3><br/>

<p>If a manageable transition to a more widely shared prosperity within
planetary boundaries is to occur, political movements and emerging parties
must balance resistance with positive advocacy of broad political,
economic, and environmental agendas. The desired result is a new social
contract that better serves owners and workers, citizens and governments,
and consumers and the planet. Business must play a central role in this
transition, recognizing that long-term stability based on shared prosperity
is of greater value than pursuing short-term profits in turbulent markets.</p>

<p>If the economy does not work well for most people, and it is threatening
the life support systems of the planet, then it seems time to rethink how
it can work better. There is no shortage of good ideas. Indeed, a large
collection of promising thought emerged during the 1960s and 1970s
addressing such issues as: the poisoning of the planet, the need to design
systems for a finite planet, the conflict between unmanaged growth and the
environment, and basic principles of sustainable economics necessary to
live within these realities.</p>

<p>There are also large literatures showing that conventional measures of
economic prosperity such as GDP do not necessarily provide good human
outcomes. Economic policies and investment incentives should instead be
calculated according to social and environmental outcomes: health,
education, nutrition, shelter, leisure time, work that puts people above
the poverty line, opportunities for people to develop their potential, and
restoration of thriving natural environments. What is the path toward these
outcomes?</p>

<h3>Solutions for Environment, Economy, and Democracy (SEED)</h3><br/>

<p>Many citizens grasp the complexity of the problems facing civilization, and
understand the many arenas -- local, national, transnational, north and
south -- in which issues must be addressed. It is time to move beyond
Twitter revolutions that produce massive expressions of anger and hope, and
develop new technology platforms that reflect political models that address
interrelated issues and scale across different geographical and cultural
boundaries. This is a proposal for developing idea networks that engage
citizens, activists, business interests, scholars, and politicians in
rethinking their visions of prosperity. The challenge is to develop more
holistic thinking that feeds more flexible political organizations, such as
hybrid movement-parties that aim to make change locally while linking their
efforts nationally and globally. </p>

<p>Finding these pathways to sustainable societies for people in different
circumstances depends on creative ideas generated by diverse knowledge
communities.  Those ideas must be communicated in compelling ways using
narratives and memes that travel across social, cultural, and political
boundaries. Smart technology and social media networks can help people
create, curate and share creative thought and action on a variety of
issues. </p>

<p>Our goal is to bring scholars, NGO practitioners, activists, and party
leaders together to reinvent a politics for the current era in which many
citizens have grown increasingly skeptical about parties, elections and
governments. Membership and voting support for traditional center-left and
center-right parties has been in decline. Far right parties are on the rise
nearly everywhere in the democracies, attracting voters who respond to
reactionary nationalist and racial discourses.  At the same time,
progressive movements and parties are experimenting with the logic of
connective action [5] that uses technology to flatten hierarchies and
engage citizens in dialogue. However, these large action networks generally
emerge as technology equipped crowds that occupy public spaces and tend to
be chaotic and short lived. What technologies would help such networks of
concerned citizens stabilize and interface better with democratic
institutions?</p>

<p>There is a key element is currently missing from this political picture: a
widely accessible information/action network for generating and sharing
ideas across diverse and geographically dispersed publics. While there are
many intellectual spaces filled with good ideas, they do not add up. The
SEED network must strive to develop intelligent open source, noncommercial,
secure technology to put people with good ideas together in an environment
that helps them bridge differences, distill thinking, and develop focused
practices. This network must have the capacity to assess successes and
failures, and develop organizational models that interface effectively with
democratic institutions.</p>

<p>There are opportunities in this historic moment if broad thinking meets
smart political organization. The question is whether compelling ideas can
bridge the critical areas of environment, economy, and democracy and offer
inclusive participatory opportunities for people who are currently divided
by tired ideology, geography, and dysfunctional politics. </p>

<h3>A Proposal for Developing a SEED Network</h3><br/>

<p>A SEED network can be built upon open source and expandable technology that
lightens the burdensome work of everyday democratic deliberation by helping
to identify and bridge differences, and distribute creative visions that
engage individuals, movements, civil society organizations, and
parties. This core platform must make it simple and inviting to develop
collaborative ideas, identify and bridge topics, build agendas, and develop
the organizational capacity to put those ideas into practice.</p>

<p>We currently have an interim website for the project, built using
off-the-shelf, open-source software. Our plan is to replace this with a
smart technology platform to support and grow a broad intellectual network
for sharing ideas and resources in the core areas of SEED. The platform
will enable semi-automated tagging and curating of content, along with
prompts for people to engage with the contributions of others
and highlight differences. Contributions that bridge multiple existing
topics, as well as popular
resolutions of differences, will rise to become the most featured and shared
content. At the same time, we will begin introducing proposals from the
network into broader intellectual and policy circles.</p>

<p>As we continue development of the platform, we will also expand the network
to include more practitioners from parties, NGOs, movements, and other
civil society organizations, with emphasis on expanding membership in the
global south. These practitioners will become equal partners in evolving
the idea agenda, as well as offering pragmatic input about obstacles and
opportunities for implementing elements of the agenda. The platform should
become an organizing hub for a set of chartering principles for a
sustainable, equitable prosperity within environmental limits, with the aim
of moving these principles into political arenas at all levels from local
to global.</p>

<p>We also plan a significant research component that examines the diffusion
of the SEED agenda by political parties, movements, and media networks that
reach broader publics, including assessment of successes and failures, and
promotion of models that work. At the same time, on the practical side we
hope to see increasing recognition by prominent leaders in different
sectors of economics, environment, politics, faith communities, business,
and popular culture, as well as translation of effective ideas and models
for action into diverse social, cultural and political settings.</p>

<p>Please visit the project website at 
<a href="https://seed.uw.edu">https://seed.uw.edu</a> for further information or to
add yourself to the project mailing list; subsequently, this same URL will
be the entry point for the smart platform as it is developed and
deployed.</p>

<p>The path to a renewal of politics and economics -- to a system that lives
within the limits of the earth -- will be a difficult one to follow, and
success is not at all guaranteed, but the alternative of continuing
business as usual will lead to a dystopian future indeed. At the same time,
many people clearly understand various aspects of these problems and want
to address them. The time is ripe for developing a viable alternative. We
invite your reactions and welcome your engagement.</p>

